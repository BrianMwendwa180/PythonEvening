# Dictionary is a collection of key-value pairs. It is unordered, changeable and indexed.


capital_cities = {
    "kenya": "Nairobi",
    "Uganda": "Kampala",
    "Tanzania": "Dodoma",
    "Rwanda": "Kigali",
    
}
print(capital_cities) # Outputs: {'kenya': 'Nairobi', 'Uganda': 'Kampala', 'Tanazania': 'Dodoma', 'Rwanda': 'Kigali'}
print(capital_cities["kenya"]) # Outputs: Nairobi